# Comparison Operation

# all results are boolean
# >,<,>=,<=,==,!=,is,is not

a = 4
b = 2

# greater than >
print('===Greater Than====')
result = a > 3
print(a, ">", 3, '=', result)
result = b > 3
print(b, ">", 3, '=', result)
result = b > 2
print(b, ">", 2, '=', result)

# less than >
print('\n===Less Than====')
result = a < 3
print(a, "<", 3, '=', result)
result = b < 3
print(b, "<", 3, '=', result)
result = b < 2
print(b, "<", 2, '=', result)
